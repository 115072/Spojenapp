package com.pls.spojenapp;

public class Obed {
    String het = "het", kedd = "kedd", szer = "szer", csut = "csut", pen = "pen", szom = "szom", vas = "vas";


    public Obed() {
    }

    public Obed(String het, String kedd, String szer, String csut, String pen, String szom, String vas) {
        this.het = het;
        this.kedd = kedd;
        this.szer = szer;
        this.csut = csut;
        this.pen = pen;
        this.szom = szom;
        this.vas = vas;
    }

    public String getHet() {
        return het;
    }


    public String getKedd() {
        return kedd;
    }


    public String getSzer() {
        return szer;
    }


    public String getCsut() {
        return csut;
    }


    public String getPen() {
        return pen;
    }


    public String getSzom() {
        return szom;
    }


    public String getVas() {
        return vas;
    }

}
